static const i32 trig[12] = {
  1024, /* cos(0.0°) * 1024 */
  1014, /* cos(8.2°) * 1024 */
  983,  /* cos(16.4°) * 1024 */
  932,  /* cos(24.5°) * 1024 */
  861,  /* cos(32.7°) * 1024 */
  774,  /* cos(40.9°) * 1024 */
  671,  /* cos(49.1°) * 1024 */
  554,  /* cos(57.3°) * 1024 */
  425,  /* cos(65.5°) * 1024 */
  286,  /* cos(73.6°) * 1024 */
  146,  /* cos(81.8°) * 1024 */
  0     /* cos(90.0°) * 1024 */
};

static i32
fast_cos(i32 angle)
{
  angle = (angle % 45);
  while (angle < 0)
  {
    angle += 45;
  }

  if (angle < 12)
  {
    return (trig[angle]);
  }
  else if (angle < 23)
  {
    return (-trig[10 - (angle - 12)]);
  }
  else if (angle < 34)
  {
    return (-trig[angle - 22]);
  }
  else
  {
    return (trig[45 - angle]);
  }
}

/* Sine based on fast cosine... */
static i32
fast_sin(i32 angle)
{
  return (-fast_cos((angle + 11) % 45));
}

