i32
fast_cos(i32 angle)
{
  angle = (angle % 45);
  while (angle < 0)
  {
    angle += 45;
  }

  if (angle < 12)
  {
    return (trig[angle]);
  }
  else if (angle < 23)
  {
    return (-trig[10 - (angle - 12)]);
  }
  else if (angle < 34)
  {
    return (-trig[angle - 22]);
  }
  else
  {
    return (trig[45 - angle]);
  }
}

/* Sine based on fast cosine... */

i32
fast_sin(i32 angle)
{
  return (-fast_cos((angle + 11) % 45));
}

