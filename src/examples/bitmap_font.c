  ei->palette[eColorBrownRust] = 0xab5236ff;
  ei->palette[eColorSoyaBean] = 0x5f574fff;

  /* Initialize font pointer array */
  {
    i32 i;
    for (i = 0; i < 96; ++i)
    {
      twtr_8x8_font_ptrs[i] = twtr_8x8_font[i];
      msx01x02_8x16_font_ptrs[i] = msx01x02_8x16_font[i];
    }
  }
