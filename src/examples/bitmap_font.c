/* Font pointer array for text_draw function */
static const u8* twtr_8x8_font_ptrs[96];
static const u8* msx01x02_8x16_font_ptrs[96];

static void
bit_draw(ExotiqueInterface* ei, const u8* sprite, i32 x, i32 y, i32 width, i32 height, color_t color)
{
  i32 row;
  for (row = 0; row < height; ++row)
  {
    i32 col;
    for (col = 0; col < width; ++col)
    {
      if (sprite[row] << col & 0x80)
      {
        if ((x + col) >= 0 && (x + col <= kScreenWidth) && (y + row) >= 0 && (y + row) <= kScreenHeight)
        {
          ei->screen[(x + col) + kScreenWidth * (y + row)] = color;
        }
      }
    }
  }
}

static void
text_draw(ExotiqueInterface* ei, const u8* font[], i32 x, i32 y, i32 width, i32 height, color_t color, const char* str)
{
  if (str)
  {
    i32 i;
    for (i = 0; str[i]; ++i)
    {
      i32 char_index = (i32)str[i] - 32;
      if (char_index >= 0 && char_index < 96)
      {
        bit_draw(ei, font[char_index], x + (width * i), y, width, height, color);
      }
    }
  }
}

static void
text_draw_sine(ExotiqueInterface* ei, const u8* font[], i32 x, i32 y, i32 width, i32 height, color_t color, const char* str)
{
  if (str)
  {
    i32 i;
    static i32 counter = 0;
    for (i = 0; str[i]; ++i)
    {
      i32 char_index = (i32)str[i] - 32;
      if (char_index >= 0 && char_index < 96)
      {
        bit_draw(ei, font[char_index], x + (width * i), y + fast_sin(counter + i * 8) / 100, width, height, color);
      }
    }
    ++counter;
  }
}

static void
memzero(u8* dest, u64 len)
{
  while (len-- > 0)
  {
    *dest++ = 0;
  }
}

