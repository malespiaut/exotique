/* Drawing primitives */

static void
rectangle_fill_draw(ExotiqueInterface* ei, i32 x1, i32 y1, i32 x2, i32 y2, u8 color)
{
  if (x1 > x2)
  {
    i32 tmp = x1;
    x1 = x2;
    x2 = tmp;
  }
  if (y1 > y2)
  {
    i32 tmp = y1;
    y1 = y2;
    y2 = tmp;
  }

  if (x1 < 0)
  {
    x1 = 0;
  }
  if (y1 < 0)
  {
    y1 = 0;
  }

  if (x2 >= kScreenWidth)
  {
    x2 = kScreenWidth - 1;
  }
  if (y2 >= kScreenHeight)
  {
    y2 = kScreenHeight - 1;
  }

  if (x1 <= x2 && y1 <= y2)
  {
    i32 y = y1;
    for (; y <= y2; ++y)
    {
      i32 x = x1;
      for (; x <= x2; ++x)
      {
        ei->screen[y * kScreenWidth + x] = color;
      }
    }
  }
}

static void
bit_draw(ExotiqueInterface* ei, const u8* sprite, i32 x, i32 y, i32 width, i32 height, color_t color)
{
  i32 row = 0;
  for (; row < height; ++row)
  {
    i32 col = 0;
    for (; col < width; ++col)
    {
      if (sprite[row] << col & 0x80)
      {
        if ((x + col) >= 0 && (x + col < kScreenWidth) && (y + row) >= 0 && (y + row) < kScreenHeight)
        {
          ei->screen[(x + col) + kScreenWidth * (y + row)] = color;
        }
      }
    }
  }
}

static void
text_draw(ExotiqueInterface* ei, i32 x, i32 y, color_t color, const char* str)
{
  if (str)
  {
    i32 i = 0;
    for (; str[i]; ++i)
    {
      i32 char_index = (i32)str[i] - 32;
      if (char_index >= 0 && char_index < 96)
      {
        bit_draw(ei, font_ptrs[char_index], x + (8 * i), y, 8, 16, color);
      }
    }
  }
}

static void
memzero(u8* dest, u64 len)
{
  while (len-- > 0)
  {
    *dest++ = 0;
  }
}

